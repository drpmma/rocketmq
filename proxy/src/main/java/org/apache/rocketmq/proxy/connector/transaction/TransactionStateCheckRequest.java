package org.apache.rocketmq.proxy.connector.transaction;

import org.apache.rocketmq.common.message.MessageExt;

public class TransactionStateCheckRequest {
    private String groupId;
    private Long tranStateTableOffset;
    private Long commitLogOffset;
    private String msgId;
    private TransactionId transactionId;
    private String brokerName;
    private MessageExt messageExt;

    public TransactionStateCheckRequest(
        String groupId,
        Long tranStateTableOffset,
        Long commitLogOffset,
        String msgId,
        TransactionId transactionId,
        String brokerName,
        MessageExt messageExt
    ) {
        this.groupId = groupId;
        this.tranStateTableOffset = tranStateTableOffset;
        this.commitLogOffset = commitLogOffset;
        this.msgId = msgId;
        this.transactionId = transactionId;
        this.brokerName = brokerName;
        this.messageExt = messageExt;
    }

    public String getGroupId() {
        return groupId;
    }

    public void setGroupId(String groupId) {
        this.groupId = groupId;
    }

    public Long getTranStateTableOffset() {
        return tranStateTableOffset;
    }

    public void setTranStateTableOffset(Long tranStateTableOffset) {
        this.tranStateTableOffset = tranStateTableOffset;
    }

    public Long getCommitLogOffset() {
        return commitLogOffset;
    }

    public void setCommitLogOffset(Long commitLogOffset) {
        this.commitLogOffset = commitLogOffset;
    }

    public String getMsgId() {
        return msgId;
    }

    public void setMsgId(String msgId) {
        this.msgId = msgId;
    }

    public TransactionId getTransactionId() {
        return transactionId;
    }

    public void setTransactionId(TransactionId transactionId) {
        this.transactionId = transactionId;
    }

    public String getBrokerName() {
        return brokerName;
    }

    public void setBrokerName(String brokerName) {
        this.brokerName = brokerName;
    }

    public MessageExt getMessageExt() {
        return messageExt;
    }

    public void setMessageExt(MessageExt messageExt) {
        this.messageExt = messageExt;
    }
}
